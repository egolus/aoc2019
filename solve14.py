class Reactor:
    store = None
    chems = None

    def __init__(self, chems):
        self.chems = chems
        self.store = defaultdict(int)

    def react(self, chemical, amount):
        if chemical == "ORE":
            self.store[chemical] -= amount
        else:
            if amount > self.store[chemical]:
                prodamount = amount - self.store[chemical]
                ins, out = self.chems[chemical]
                times = math.ceil(prodamount/out)
                self.store[chemical] += times * out
                for c in ins:
                    self.react(c[1], times * c[0])
            self.store[chemical] -= amount

    def needed(self):
        return self.store["ORE"] * -1


def solve_a(data):
    chems = {}
    for line in data.splitlines():
        ins, out = line.split("=>")
        out = out.strip().split(" ")
        out[0] = int(out[0])

        ix = ins.split(",")
        for i, _ in enumerate(ix):
            ix[i] = ix[i].strip().split(" ")
            ix[i][0] = int(ix[i][0])
        chems[out[1]] = (ix, out[0])
    reactor = Reactor(chems)
    reactor.react("FUEL", 1)
    amount = -1 * reactor.store["ORE"]
    return amount


def solve_b(data):
    chems = {}
    for line in data.splitlines():
        ins, out = line.split("=>")
        out = out.strip().split(" ")
        out[0] = int(out[0])

        ix = ins.split(",")
        for i, _ in enumerate(ix):
            ix[i] = ix[i].strip().split(" ")
            ix[i][0] = int(ix[i][0])
        chems[out[1]] = (ix, out[0])

    ore = 1000000000000
    min_ = 0
    max_ = ore

    while max_ - min_ > 1:
        times = (max_ - min_) // 2 + min_
        reactor = Reactor(chems)
        reactor.react("FUEL", times)
        if reactor.needed() > ore:
            max_ = times
        else:
            min_ = times
            max_ += times
    return min_


